package clase2;

public class Complejo {

    private float r,i;

    public Complejo(float r, float i){
        this.r = r;
        this.i = i;
    }

    public Complejo(){

    }

    public float getR() {
        return r;
    }

    public void setR(float r) {
        this.r = r;
    }

    public float getI() {
        return i;
    }

    public void setI(float i) {
        this.i = i;
    }
}
